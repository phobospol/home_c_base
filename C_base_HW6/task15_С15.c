#include <stdio.h>
//Составить функцию логическую функцию, которая определяет, верно ли, что в заданном числе все цифры стоят по возрастанию. Используя данную функцию решить задачу. int grow_up(int n)

int grow_up(int n)// Функция возвращает 1, если цифры числа растут, иначе 0
{
    if (n < 10)  // одноцифровое число всегда "растущее"
        return 1;

    int prev = n % 10;  // последняя цифра
    n /= 10;

    while (n > 0)
    {
        int curr = n % 10;  // текущая цифра слева
        if (curr >= prev)   // цифры не растут
            return 0;

        prev = curr;
        n /= 10;
    }

    return 1;  // все цифры по возрастанию
}

int main()
{
    int n;
    scanf("%d", &n);

    printf("%s\n", grow_up(n) ? "YES" : "NO");

    return 0;
}

